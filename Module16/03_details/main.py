shop = [['каретка', 1200], ['шатун', 1000], ['седло', 300],
        ['педаль', 100], ['седло', 1500], ['рама', 12000],
        ['обод', 2000], ['шатун', 200], ['седло', 2700]]

name = input('Название детали: ')
count = 0
summ = 0

for char in shop:
        if char[0] == name:
                count += 1
                summ += char[1]

print(f'Кол-во деталей - {count}')
print(f'Общая стоимость - {summ}')